def _get_ssm_outputs(namespace: str, region: str) -> dict[str, str]:
    """Read all shared-services parameters from SSM under the given namespace."""
    ssm = boto3.client("ssm", region_name=region)
    path = f"/shared-services/{namespace}/"
    params: dict[str, str] = {}
    try:
        paginator = ssm.get_paginator("get_parameters_by_path")
        for page in paginator.paginate(Path=path, Recursive=True):
            for p in page["Parameters"]:
                relative_key = p["Name"].removeprefix(path)
                params[relative_key] = p["Value"]
    except NoCredentialsError:
        print("[red]Error: No AWS credentials found. Configure your AWS profile or environment.[/red]")
        raise typer.Exit(1)
    except ClientError as e:
        print(f"[red]AWS error reading SSM: {e}[/red]")
        raise typer.Exit(1)
    return params


def _git_remote_repo() -> Optional[str]:
    """Parse 'owner/repo' from git remote origin URL."""
    result = subprocess.run(
        "git remote get-url origin",
        shell=True,
        capture_output=True,
        text=True,
        check=False,
    )
    if result.returncode != 0:
        return None
    url = result.stdout.strip()
    # SSH:   git@github.com:owner/repo.git
    # HTTPS: https://github.com/owner/repo.git
    match = re.search(r"[:/]([^/]+/[^/]+?)(?:\.git)?$", url)
    return match.group(1) if match else None


def _write_backend_hcl(
    output_dir: Path,
    bucket: str,
    github_org: str,
    github_repo: str,
    team_guid: str,
    region: str,
) -> Path:
    """Write backend.hcl with the team's scoped state key."""
    path = output_dir / "backend.hcl"
    path.write_text(
        f'bucket       = "{bucket}"\n'
        f'key          = "teams/{github_org}/{github_repo}/{team_guid}/terraform.tfstate"\n'
        f'region       = "{region}"\n'
        f"use_lockfile = true\n"
        f"encrypt      = true\n"
    )
    return path


def _write_auto_tfvars(output_dir: Path, outputs: dict[str, str]) -> Path:
    """Write shared-services.auto.tfvars with resolved platform outputs."""
    path = output_dir / "shared-services.auto.tfvars"
    lines: list[str] = [
        "# Generated by team-setup — do not edit manually.\n",
        "# Re-run: uv run team-setup/main.py init ... to refresh.\n\n",
    ]

    ecr_push_role = outputs.get("dev/ecr-push-role-arn", "")
    if ecr_push_role:
        lines.append(f'ecr_push_role_arn = "{ecr_push_role}"\n')

    ecr_urls = {
        key.removeprefix("dev/ecr/").replace("--", "/"): val
        for key, val in outputs.items()
        if key.startswith("dev/ecr/")
    }
    if ecr_urls:
        lines.append("\necr_repository_urls = {\n")
        for name, url in sorted(ecr_urls.items()):
            lines.append(f'  "{name}" = "{url}"\n')
        lines.append("}\n")

    path.write_text("".join(lines))
    return path


def _resolve_repo(repo: Optional[str]) -> str:
    """Return the GitHub 'owner/repo' string, auto-detecting from git remote if not given."""
    detected = repo or _git_remote_repo()
    if not detected:
        print("[red]Error: Could not detect GitHub repo from git remote. Pass --repo owner/repo.[/red]")
        raise typer.Exit(1)
    return detected


