"""
Team bootstrap CLI for consuming shared-services platform outputs.

Generates backend.hcl for the team's own Terraform state and writes the
required CI variables and secrets to the team's GitHub repository. Reads
platform values from AWS SSM Parameter Store — no shared-services clone needed.

Usage:
  uv run team-setup/main.py run --team-slug team-payments
  uv run team-setup/main.py init --team-slug team-payments --output-dir ./terraform
  uv run team-setup/main.py show-outputs
"""

import json
import re
import subprocess
from pathlib import Path
from typing import Optional

import boto3
import typer
from lib.github import get_github_repo
from botocore.exceptions import ClientError, NoCredentialsError
from github import GithubException
from rich import print
from rich.console import Console
from rich.table import Table

app = typer.Typer(
    name="team-setup",
    help="Bootstrap a team repository to consume shared-services platform outputs.",
    no_args_is_help=True,
)
console = Console()


